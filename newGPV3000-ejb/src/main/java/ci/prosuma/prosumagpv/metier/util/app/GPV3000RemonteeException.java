package ci.prosuma.prosumagpv.metier.util.app;

/**
 * @author aadama
 *
 */
public class GPV3000RemonteeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1579699338240951062L;

	/**
	 * 
	 */
	public GPV3000RemonteeException() {
		super();
	}

	/**
	 * @param message
	 */
	public GPV3000RemonteeException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public GPV3000RemonteeException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public GPV3000RemonteeException(String message, Throwable cause) {
		super(message, cause);
	}

}
